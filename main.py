# 3
even_or_odd = lambda x: "Even" if x % 2 == 0 else "Odd"
print(even_or_odd(7))

# 4
max_of_two = lambda x, y: x if x > y else y
print(max_of_two(10, 20))

# 5
number_7 = lambda x: True if x > 7  else False
